import i18n from "i18next"
import { initReactI18next } from "react-i18next"

export const supportedLanguages = ["ru", "en"] as const
export type SupportedLanguage = (typeof supportedLanguages)[number]

export const languageStorageKey = "sb0rka.console.language"

const isSupportedLanguage = (value: string): value is SupportedLanguage =>
  supportedLanguages.includes(value as SupportedLanguage)

const normalizeLanguage = (value?: string | null): SupportedLanguage | null => {
  if (!value) return null
  const language = value.toLowerCase().split("-")[0]
  return isSupportedLanguage(language) ? language : null
}

const getStoredLanguage = (): SupportedLanguage | null => {
  if (typeof window === "undefined") return null
  return normalizeLanguage(window.localStorage.getItem(languageStorageKey))
}

const getBrowserLanguage = (): SupportedLanguage | null => {
  if (typeof navigator === "undefined") return null
  for (const language of navigator.languages ?? [navigator.language]) {
    const normalized = normalizeLanguage(language)
    if (normalized) return normalized
  }
  return null
}

export const getResolvedLanguage = (): SupportedLanguage => {
  const language = normalizeLanguage(i18n.resolvedLanguage ?? i18n.language)
  return language ?? "ru"
}

